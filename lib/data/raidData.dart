class RaidTarget {
  const RaidTarget({
    required this.id,
    required this.label,
    this.subLabel,
    required this.tier,
    required this.category,
    required this.hp,
    required this.img,
    required this.cost,
    this.explicitCostsOnly = false,
  });

  final String id;
  final String label;
  final String? subLabel;
  final Tier tier;
  final RaidCategory category;
  final int hp;
  final String img;
  final Map<ExplosiveType, int> cost;
  final bool explicitCostsOnly;
}

